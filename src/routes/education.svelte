<b>Math Major</b><br />
<i>Relevant courses:</i>
<ul>
    <li>Linear Algebra</li>
    <span class="desc">Fields, complex numbers, vector spaces over a field, linear transformations, matrix of a linear transformation, kernel, range, dimension theorem, isomorphisms, change of basis, eigenvalues, eigenvectors, diagonalizability, real and complex inner products, spectral theorem, adjoint/self-adjoint/normal linear operators, triangular form, nilpotent mappings, Jordan canonical form.</span>
    <li>Graph Theory</li>
    <span class="desc">Graphs, subgraphs, isomorphism, trees, connectivity, Euler and Hamiltonian properties, matchings, vertex and edge colourings, planarity, network flows and strongly regular graphs. Execute applications to such problems as timetabling, tournament scheduling, experimental design and finite geometries.</span>
