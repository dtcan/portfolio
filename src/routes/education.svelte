    <span class="desc">First and second order conditions for finite and infinite dimensional optimization problems. Lagrange multipliers, Kuhn-Tucker conditions, convexity and calculus variations. Basic numerical search methods.</span>
    <li>Ordinary Differential Equations</li>
    <span class="desc">First order ordinary differential equations: Direction fields, integrating factors, separable equations, homogeneous equations, exact equations, autonomous equations, modeling. Existence and uniqueness theorem. Higher order equations: Constant coefficient equations, reduction of order, Wronskian, method of undetermined coefficients, variation of parameters. Solutions by series and integrals. First order linear systems, fundamental matrices. Non-linear equations, phase plane, stability.</span>
</ul>
