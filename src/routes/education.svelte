<i>Relevant courses:</i>
<ul>
    <li>Software Engineering</li>
    <span class="desc">Basic software development infrastructure; requirements elicitation and tracking; estimation and prioritization; teamwork skills; basic UML; design patterns and refactoring; security, discussion of ethical issues, and professional responsibility.</span>
    <li>Data Structures and Analysis</li>
    <span class="desc">Algorithm analysis: worst-case, average-case, amortized complexity, expected worst-case complexity. Standard abstract data types, such as graphs, dictionaries, priority queues, and disjoint sets. A variety of data structures for implementing these abstract data types, such as balanced search trees, hashing, heaps, and disjoint forests. Design and comparison of data structures.</span>
